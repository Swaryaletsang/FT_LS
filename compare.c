#include "ft_ls.h"

int		compare(void *s1, void *s2)
{
	int		ret;
	char	*str1;
	char	*str2;

	str1 = (char *)s1;
	str2 = (char *)s2;
	ret = ft_strcmp(str1, str2);
	return (ret);
}
